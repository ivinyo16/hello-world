#include<stdio.h>
#include<string.h>
#include <stdlib.h>

int main(int argc, char* argv[])
{



int counter=1; /* define counter */
/* initialization, repetition condition, and increment
are all included in the for statement header. */
for ( counter ; counter <= 10; counter++ ) {
printf( "%d\n", counter );
}



	

	return 0;
}
