void printMaze(int maze[][12])
{
	int i,j;
	for(i = 0 ; i < 12 ; i++)
	{
		for( j = 0; j < 12 ; j++)
		{
			printf("%c ", maze[i][j]);
		}
		printf("\n");
	}
}

int mazeTraverse(int maze[][12], int x, int y)
{
	int *ptr = &maze[x][y];
	if(*ptr == '#')
		return -1;
	//first hop (right)
	if(*(ptr+1) != '#')
	{
		ptr++;
		x++;
	}
	//bottom
	else if(*(ptr+12) != '#')
	{
		ptr+=12;
		y++;
	}
	else if(*(ptr-12) != '#')
	{
		ptr = ptr -12;
		y--;
	}
	else if(*(ptr-1) != '#')
	{
		ptr--;
		x--;
	}


//	int **dptr = &ptr;
//	printf("here %c\n", maze[x][y]);
//	ptr+=12;
//	printf("*ptr %c\n", *ptr);
	return 1;
}

